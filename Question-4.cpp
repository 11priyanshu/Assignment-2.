#include<iostream>
using namespace std;
int main(){
    float Radius;
    cout<<"Enter Radius : ";
    cin>>Radius;
    float Height;
    cout<<"Enter Height : ";
    cin>>Height;
    float Volume = 3.1415*Radius*Radius*Height;
    cout<<"Volume Of Cylinder Is : ";
    cout<<Volume;

}